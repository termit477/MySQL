FROM auto 
WHERE MARK = 'BMW' OR MARK = 'LADA'
GROUP BY MARK, COLOR;

-- 2. Вывести марку авто и количество ато не этой марки
-- 100 машин. 20 бмв, 80 другой. ауди 30 и 70 другой, лада 15 и 85 другой
SELECT MARK, COUNT(*) AS count_mark,
(SELECT COUNT(*) FROM auto) - COUNT(MARK) AS other
FROM auto 
GROUP BY MARK;

